public class Horse
{
	String name;
	String color;
	int year;
	
	public void setName(String n)
	{
		name = n;
	}
	
	public void setColor(String c)
	{
		color = c;
	}
	
	public void setYear(int y)
	{
		year = y;
	}
	
	public String getName()
	{
		return name;
	}
	
	public String getColor()
	{
		return color;
	}
	
	public int getYear()
	{
		return year;
	}
}